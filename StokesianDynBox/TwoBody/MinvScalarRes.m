function [res] = MinvScalarResFT(s)

s2 = s * s;
s3 = s * s2;
s4 = s2 * s2;
s6 = s3 * s3;

dx = 4.0 * s6 - (3.0 * s2 - 2.0) * (3.0 * s2 - 2.0);
dy = 4.0 + s2 *     ...
(- 12.0 + s2 *      ...
(9.0 + s2 *         ...
(- 32.0 + s2 *      ...
(- 144.0 + s2 *     ...
(- 36.0 + s2 *      ...
(64.0))))));

xa11 = 4.0 * s6 / dx;
xa12 = - 2.0 * s3 * (3.0 * s2 - 2.0) / dx;
ya11 = 16.0 * s6 * ((4.0 * s4 - 3.0) * s2 - 1.0) / dy;                                                           ...
ya12 = - 4.0 * s3 * (((12.0 * s2 + 8.0) * s4 + 3.0) * s2 - 2.0) / dy;
yb11 = - 16.0 * s6 * s * (3.0 * s2 + 4.0) / dy;                                           ...
yb12 = 8.0 * s4 * ((8.0 * s4 - 3.0) * s2 + 2.0) / dy;
xc11 = 4.0 * s6 / 3.0 / (s6 - 1.0);
xc12 = - 4.0 * s3 / 3.0 / (s6 - 1.0);
yc11 = 16.0 * s6 * (((16.0 * s2 - 9.0) * s2 - 24.0) * s2 - 4.0) / 3.0 / dy;
yc12 = 8.0 * s3 * ((16.0 * s2 + 9.0) * s4 - 4.0) / 3.0 / dy;

res = [xa11,xa12,ya11,ya12,yb11,yb12,xc11,xc12,yc11,yc12]';
res = [res;zeros(12,1)];


return
end

